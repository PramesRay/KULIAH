
import java.util.Scanner;
public class Sementara {
  private String npm;
  private String kode;
  private int nilai;
  
  public Sementara() {
    this.npm = null;
    this.kode = null;
    this.nilai = 0;
  }

  public String getNpm() {
    return npm;
  }

  public void setNpm(String npm) {
    this.npm = npm;
  }

  public String getKode() {
    return kode;
  }

  public void setKode(String kode) {
    this.kode = kode;
  }

  public int getNilai() {
    return nilai;
  }

  public void setNilai(int nilai) {
    this.nilai = nilai;
  }
}
