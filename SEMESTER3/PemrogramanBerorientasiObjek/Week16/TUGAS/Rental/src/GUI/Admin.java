    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Koneksi kon = new Koneksi(); 
        ResultSet rs = null; 
        int i = 0; 
        try{ 
            rs = kon.getData("SELECT * FROM peminjaman");
            
            while(rs.next()){
                reportTable.setValueAt(rs.getString("id_peminjaman")    , i, 0);
                reportTable.setValueAt(rs.getString("id_pelanggan")     , i, 1);
                reportTable.setValueAt(rs.getString("id_mobil")         , i, 2);
                reportTable.setValueAt(rs.getString("id_petugas")       , i, 3);
                reportTable.setValueAt(rs.getString("tgl_pinjam")       , i, 4);
                reportTable.setValueAt(rs.getString("tgl_hrs_kembali")  , i, 5);
                reportTable.setValueAt(rs.getString("biaya")            , i, 6);
                reportTable.setValueAt(rs.getString("denda")            , i, 7); 
                i++; 
            } 
            rs.close(); 
        }
        catch(SQLException e){
            javax.swing.JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened
   
    
    
