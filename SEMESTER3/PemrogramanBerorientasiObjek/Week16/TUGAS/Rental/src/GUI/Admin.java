    private void searchPegawaiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchPegawaiButtonActionPerformed
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        
        try{ 
            rs = kon.getData("SELECT * FROM pegawai WHERE id_pegawai = '" +  idPegField_search.getText() + "'");
            while(rs.next()){
                idPegField_result.setText(rs.getString("id_pegawai"));
                namaPegField_result.setText(rs.getString("nama"));
                cpPegField_result.setText(rs.getString("cp"));
                alamatPegTA_result.setText(rs.getString("alamat"));
                usnField_result.setText(rs.getString("username"));
                pwField_result.setText(rs.getString("password"));
                kodeField_result.setText(rs.getString("kode"));
                
                javax.swing.JOptionPane.showMessageDialog(null, "Search Success");
                
                idPegField_search.setText(null); 
                idPegField_result.setEnabled(false);
                namaPegField_result.setEnabled(true);
                alamatPegTA_result.setEnabled(true);
                cpPegField_result.setEnabled(true);
                usnField_result.setEnabled(true);
                pwField_result.setEnabled(true);
                kodeField_result.setEnabled(true);
                updateButton_tab3.setEnabled(true); 
                deleteButton_tab3.setEnabled(true);
            } 
            rs.close(); 
        }
        catch(SQLException e){ 
            javax.swing.JOptionPane.showMessageDialog(null,"Error:" + e.getMessage()); 
        }
    }//GEN-LAST:event_searchPegawaiButtonActionPerformed

