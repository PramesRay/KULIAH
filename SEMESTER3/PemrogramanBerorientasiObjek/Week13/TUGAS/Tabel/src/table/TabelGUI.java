    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        try{
            ubahButton.setEnabled(true);
            hapusButton.setEnabled(true);
            baruButton.setEnabled(true);
            simpanButton.setEnabled(false);
            batalButton.setEnabled(true);
            
            tampilRowTable();
            
            int tanya=JOptionPane.showConfirmDialog(null, "Yakin dihapus?","Hapus",1);
            //atau int tanya=JOptionPane.showConfirmDialog(null,"Yakin dihapus ?");
            if (tanya==0){
                 int row;
                row = mahasiswaTable.getSelectedRow();
                 mahasiswaTable.remove(row);
                // mahasiswaTable.validate();
                // PENGHAPUSAN DENGAN DATA BASE
                counter--;
            }
        }
        catch (Throwable t){
            ubahButton.setEnabled(true);
            hapusButton.setEnabled(true);
            baruButton.setEnabled(true);
            simpanButton.setEnabled(false);
            batalButton.setEnabled(true);
            JOptionPane.showMessageDialog(null,"Sorot dulu datanya, " + "silahkan dipilih yg dihapus");
        }
    }//GEN-LAST:event_hapusButtonActionPerformed

