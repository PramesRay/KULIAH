    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        int barisSimpan,row;
        
        ubahButton.setEnabled(true);
        hapusButton.setEnabled(true);
        baruButton.setEnabled(true);
        simpanButton.setEnabled(false);
        batalButton.setEnabled(false);
        getIsianKeArray();

        if ((data[0].isEmpty()) || (data[1].isEmpty()) ||(data[2].isEmpty()) || (data[3].isEmpty())){
            JOptionPane.showMessageDialog(null,"Masih ada nilai yang kosong, " + "silahkan dilengkapi");
            npmField.requestFocus();
            return;
        }
        else {
            if (status==1){
                barisSimpan=counter++; // baru
            }
            else { // status =2 ubah
                row=mahasiswaTable.getSelectedRow();
                barisSimpan=row;
            }
            
            for (int i=0;i<4;i++){ // i menyatakan kolom
                mahasiswaTable.setValueAt(data[i], barisSimpan, i);
            }
        }
        counterLabel.setText("Counter Data :"+Integer.toString(counter));
        setDisabledKomponen();
        bersihKomponen();
        mahasiswaTable.setEnabled(true);
        ubahButton.setEnabled(true);
        hapusButton.setEnabled(true);
        baruButton.setEnabled(true);
        simpanButton.setEnabled(false);
        batalButton.setEnabled(false); 
    }//GEN-LAST:event_simpanButtonActionPerformed

    private void mahasiswaTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mahasiswaTableMouseClicked
        try {
            if(evt.getClickCount() == 2){
                setDisabledKomponen();
                tampilRowTable();
            }
        }
        catch (Throwable t){
            JOptionPane.showMessageDialog(null,"Record Kosong... ");
        }

    }//GEN-LAST:event_mahasiswaTableMouseClicked

    private void ubahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahButtonActionPerformed
        try{
            tampilRowTable();
            setEnabledKomponen();
           
            npmField.setEnabled(false); //npm tdk bisa di edit
            namaField.requestFocus();
            mahasiswaTable.setEnabled(false);
            
            getIsianKeArray();
            
            ubahButton.setEnabled(false);
            hapusButton.setEnabled(false);
            baruButton.setEnabled(false);
            simpanButton.setEnabled(true);
            batalButton.setEnabled(true);
            status = 2; // edit
        }
        catch (Throwable t){
            JOptionPane.showMessageDialog(null,"Sorot dulu datanya, " + "silahkan dipilih yg diedit");

            hapusButton.setEnabled(true);
            baruButton.setEnabled(true);
            simpanButton.setEnabled(false);
            batalButton.setEnabled(false);
        }

    }//GEN-LAST:event_ubahButtonActionPerformed

