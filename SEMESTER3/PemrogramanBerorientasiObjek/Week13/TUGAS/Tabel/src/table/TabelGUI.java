    private void bersihKomponen(){
        npmField.setText("");
        namaField.setText("");
        priaRadio.setSelected(false);
        wanitaRadio.setSelected(false);
    }
    
    private void getIsianKeArray(){
        String jenkel="";
        if (priaRadio.isSelected()) {
            jenkel="Laki-laki";
        }else {
            jenkel="Perempuan";
        }
        
        data[0]=npmField.getText();
        data[1]=namaField.getText();
        data[2]=jenkel;
        data[3]=prodiComboBox.getSelectedItem().toString();
    }
    
    public void tampilRowTable() {
        int row;
        row = mahasiswaTable.getSelectedRow();

        npmField.setText(mahasiswaTable.getValueAt(row, 0).toString());
        namaField.setText(mahasiswaTable.getValueAt(row, 1).toString());
        
        if (mahasiswaTable.getValueAt(row, 2).toString().equals("Laki-laki")){
            priaRadio.setSelected(true);
            wanitaRadio.setSelected(false);
        }else {
            priaRadio.setSelected(false);
            wanitaRadio.setSelected(true);
        }
        
        prodiComboBox.setSelectedItem(mahasiswaTable.getValueAt(row,3).toString());
    }


    
