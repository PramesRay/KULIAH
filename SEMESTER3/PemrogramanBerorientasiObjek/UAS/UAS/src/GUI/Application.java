    private void searchButton_mhsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButton_mhsActionPerformed
        Koneksi kon = new Koneksi();
        ResultSet rs = null;
        String thn, bln, tgl;
        try{
            rs = kon.getData("SELECT * FROM mahasiswa WHERE npm ='"+npm_cari.getText()+"'");
            while(rs.next()){
                npm_hasil.setText(rs.getString("npm"));
                nama_hasil.setText(rs.getString("nama_mhs"));

                if("Laki-Laki".equals(rs.getString("jenis_kelamin"))) {
                    priaRadio_hasil.setSelected(true);
                } else {
                    wanitaRadio_hasil.setSelected(true);
                }

                String[] tglLahir = rs.getString("tgl_lahir").split("-", 3);
                thn = tglLahir[0];
                bln = tglLahir[1];
                tgl = tglLahir[2];

                tgl_hasil.setSelectedIndex(Integer.parseInt(tgl));
                bln_hasil.setSelectedIndex(Integer.parseInt(bln));
                tahun_hasil.setText(thn);
                telp_hasil.setText(rs.getString("no_telp"));
                alamat_hasil.setText(rs.getString("alamat"));

                javax.swing.JOptionPane.showMessageDialog(null, "Search Success");

                npm_cari.setText(null);
                updateButton_mhs.setEnabled(true);
                deleteButton_mhs.setEnabled(true);
            }
            rs.close();
        }
        catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, "Error:"+e.getMessage());
        }
    }//GEN-LAST:event_searchButton_mhsActionPerformed

