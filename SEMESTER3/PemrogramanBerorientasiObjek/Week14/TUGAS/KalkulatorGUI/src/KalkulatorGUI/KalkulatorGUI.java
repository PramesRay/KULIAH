     public void getOperand(javax.swing.JButton button){
        operand+=button.getText();
        obj.setOperand(operand);
        inputField.setText(operand);
    }
     
    private void getOperator(int opt){
        obj.setOperator(opt);
        operand="";
    }
    private void proses(){
        DecimalFormat df = new DecimalFormat("#,###.########");
        obj.process();
        operand = "";
        inputField.setText(df.format(obj.getHasil())+"");
    }
    
    private void Bersih(){
        operand = "";
        obj.setOperator(0);
        obj.setHasil(0);
        inputField.setText("0");
    }



